var number1 = 20;
var number2 = 10;

var addition = add(number1, number2);
console.log(number1 + " + " + number2 + " = " + addition);

var subtraction = subtract(number1, number2);
console.log(number1 + " - " + number2 + " = " + subtraction);

var multiplication = multiply(number1, number2);
console.log(number1 + " * " + number2 + " = " + multiplication);

var division = divide(number1, number2);
console.log(number1 + " / " + number2 + " = " + division);